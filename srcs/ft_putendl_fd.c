#include "libft.h"

void	ft_putendl_fd(const char *s, int fd)
{
	char	*new;
	size_t	size;

	new = ft_strjoin(s, "\n");
	size = ft_strlen(new);
	if (new != NULL)
	{
		write(fd, new, size);
		ft_strdel(&new);
	}
}
