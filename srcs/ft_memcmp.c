#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	char	*c1;
	char	*c2;

	i = 0;
	c1 = (char *)s1;
	c2 = (char *)s2;
	while (i < n)
	{
		if (*c1 != *c2)
			return (*c1 - *c2);
		++i;
		++c1;
		++c2;
	}
	return (0);
}
