#include "libft.h"

char	*ft_strcpy(char *dest, const char *src);

char	*ft_strdup(const char *s)
{
	char	*dup;
	
	if (!(dup = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1))))
		return (NULL);
	ft_strcpy(dup, s);
	return (dup);
}
