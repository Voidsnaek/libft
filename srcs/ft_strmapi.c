#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char		*new;
	size_t		size;
	unsigned int	i;

	size = ft_strlen(s);
	new = ft_strnew(size);
	if (new == NULL)
		return (NULL);
	while (*s)
	{
		*new = f(i, *s);
		++new;
		++s;
		++i;
	}
	return (new - size);
}
