#include "libft.h"

char	*ft_strnew(size_t size)
{
	char		*new;
	unsigned int	i;
	
	if(!(new = (char *)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	i = 0;
	while (i < size + 1)
	{
		*new = '\0';
		++new;
		++i;
	}
	*new = '\0';
	return (new - size - 1);
}
