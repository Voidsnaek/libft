#include "libft.h"

void	ft_putendl(char const *s)
{
	char		*new;
	char		**start;

	new = ft_strjoin(s, "\n");
	start = &new;
	write(1, new, ft_strlen(new));
	if (new != NULL)
		free(*start);
}
