#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char *new;
	size_t size;
	
	size = ft_strlen(s1) + ft_strlen(s2);
	if (!(new = (char *)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	new[size] = '\0';
	while (*s1 && *new)
	{
		*new = *s1;
		++new;
		++s1;
	}
	while (*s2 && *new)
	{
		*new = *s2;
		++new;
		++s2;
	}
	return (new - size);
}
