#include "libft.h"

void	*ft_memalloc(size_t size)
{
	size_t	i;
	void	*new;
	char	*ptr;

	if(!(new = malloc(size)))
		return (NULL);
	ptr = (char *)new;
	i = 0;
	while (i < size)
	{
		ptr = '\0';
		++ptr;
		++i;
	}
	return (new - size);
}
