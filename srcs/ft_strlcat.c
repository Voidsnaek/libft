#include "libft.h"

size_t	strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	while (*dst && (size - i) > 0)
	{
		++i;
	}
	while (*src && (size - i) > 0)
	{
		*dst = *src;
		++dst;
		++src;
		++i;
	}
	if (*src)
		return (i);
	else
	{
		*dst = '\0';
		return (i);
	}
}
